    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        
        ClienteBL logica = new ClienteBL();
        if(empty(txtNombres)){
            JOptionPane.showMessageDialog(null,"Ingrese el nombre del cliente");
        }else if(empty(txtApellidos)){
            JOptionPane.showMessageDialog(null,"Ingrese el apellido del cliente");
        }else if(empty(txtDNI)){
            JOptionPane.showMessageDialog(null,"Ingrese el DNI del cliente");
        }else if(empty(txtTelefono)){
            JOptionPane.showMessageDialog(null,"Ingrese el teléfono del cliente");
        }else if(empty(txtCorreo)){
            JOptionPane.showMessageDialog(null,"Ingrese el correo del cliente");
        }else{
            ClienteNatural x = new ClienteNatural();
            x.setNombre(txtNombres.getText());
            x.setApellido(txtApellidos.getText());
            x.setDni(txtDNI.getText());
            x.setTelefono(txtTelefono.getText());
            x.setEmail(txtCorreo.getText());
            if(logica.agregarCliente(x)){
                JOptionPane.showMessageDialog(null, "Cliente agregado correctamente");
            }else{
                JOptionPane.showMessageDialog(null,"No se pudo agregar al cliente");
            }
        }
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

